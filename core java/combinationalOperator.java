
// ============================ COMBINATIONAL OPERATORS ==================================

public class combinationalOperator {
    public static void main(String[] args) {
        
        int a = 5;
        a+=3;
        System.out.println(a);

        int b = 5;
        b -= 2;
        System.out.println(b);

        int c = 5;
        c *= 3;
        System.out.println(c);

        int d = 17;
        d /= 3;
        System.out.println(d);

        int e = 17;
        e %= 3;
        System.out.println(e);
    }
}
